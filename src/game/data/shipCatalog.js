export const SHIP_CATALOG = [
  {
    serial: 0,
    code: '#0',
    id: 'player',
    name: 'Player',
    role: 'player',
    exhaustId: null,
    exhaustName: null,
    theme: PLAYER_SHIP_THEME,
  },
  ...EXHAUST_PLUGINS.map((plugin, index) => ({
    serial: index + 1,
    code: `#${index + 1}`,
    id: `enemy-${plugin.id}`,
    name: plugin.name,
    role: 'enemy',
    exhaustId: plugin.id,
    exhaustName: plugin.name,
    theme: ENEMY_SHIP_THEMES[plugin.id] ?? ENEMY_SHIP_THEMES['laser-confetti-classic'],
  })),
]

export const getEnemyCatalogEntryByPluginIndex = (pluginIndex) => {
  const safeIndex =
    ((pluginIndex % EXHAUST_PLUGINS.length) + EXHAUST_PLUGINS.length) % EXHAUST_PLUGINS.length
  return SHIP_CATALOG[safeIndex + 1]
}
